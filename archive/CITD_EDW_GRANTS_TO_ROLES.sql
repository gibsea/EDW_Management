select	TABLE_CATALOG
,		TABLE_SCHEMA
,		NAME
,       GRANTED_BY
,		GRANTED_TO
,		GRANTEE_NAME
,		PRIVILEGE
,		GRANT_OPTION
,       CREATED_ON
,		DELETED_ON
,		MODIFIED_ON
,       OBJECT_INSTANCE
from	SNOWFLAKE.ACCOUNT_USAGE.GRANTS_TO_ROLES
where	NAME			like '%'
AND		TABLE_CATALOG	like '%'
AND		TABLE_SCHEMA	like '%'
AND     GRANTEE_NAME    like 'CITD_GUEST'
order	by
		TABLE_CATALOG
,		TABLE_SCHEMA
,		NAME
,       GRANTED_BY
,		GRANTED_TO
,		GRANTEE_NAME
,		PRIVILEGE
,		GRANT_OPTION
,       CREATED_ON          desc
,       OBJECT_INSTANCE
;